/**
 * Objeto modelo para usuários
 */
class ListaModel{
    /**
     * Construtor do objeto modelo para usuários
     * @param {Array}   
     */
    constructor(lista, lista_ordenada){
        lista = []
        lista_ordenada = []
        this.lista = lista
        this.lista_ordenada = lista_ordenada
    }
}

export default ListaModel